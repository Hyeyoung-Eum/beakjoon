N=int(input())

#분해합이 9다! 생성자 : 9
#분해합이 15다! 생성자 12 12+1+2
#분해합 216 195 198=1+9+8


# 10 = 10+1+0=11
# 11 = 11+1+1=13
# 12 = 12+1+2=15
# 13 = 13+1+3=17
# 14 = 14+1+4=19
# 15 = 15+1+5=21
# 16 = 16+1+6=23

# 20 = 20+2+0=22
# 21 = 21+2+1=24
# 101 = 101+1+1=103
# 102 = 102+1+2=105

x=0
for i in range(1, N+1):
    a=list(map(int, str(i))) #i를 list로 만들건데! int 형식으로 바꿀거야
    if N == sum(a) + i:
        x=i
        break;
print(x)
